'use strict';

const fs = require('node:fs');
const path = require('node:path');

const projectRoot = path.resolve(__dirname, '..');
const fontDirectory = path.join(projectRoot, 'src', 'ApplyWise.Web', 'wwwroot', 'lib', 'pdfmake', 'fonts');
const outputPath = path.join(projectRoot, 'src', 'ApplyWise.Web', 'wwwroot', 'lib', 'pdfmake', 'libre_baskerville_vfs.js');
const files = [
    'LibreBaskerville-Variable.ttf',
    'LibreBaskerville-Italic-Variable.ttf'
];

const vfs = Object.fromEntries(files.map((filename) => [
    filename,
    fs.readFileSync(path.join(fontDirectory, filename)).toString('base64')
]));
const fonts = {
    LibreBaskerville: {
        normal: 'LibreBaskerville-Variable.ttf',
        bold: 'LibreBaskerville-Variable.ttf',
        italics: 'LibreBaskerville-Italic-Variable.ttf',
        bolditalics: 'LibreBaskerville-Italic-Variable.ttf'
    }
};

const source = `/* Generated by tools/generate-resume-serif-font-vfs.cjs. Libre Baskerville is licensed under the SIL Open Font License. */\n`
    + `var vfs = ${JSON.stringify(vfs)};\n`
    + `var fonts = ${JSON.stringify(fonts)};\n`
    + `var _global = typeof window === 'object' ? window : typeof global === 'object' ? global : typeof self === 'object' ? self : this;\n`
    + `if (typeof _global.pdfMake !== 'undefined' && typeof _global.pdfMake.addVirtualFileSystem === 'function') { _global.pdfMake.addVirtualFileSystem(vfs); }\n`
    + `if (typeof _global.pdfMake !== 'undefined' && typeof _global.pdfMake.addFonts === 'function') { _global.pdfMake.addFonts(fonts); }\n`
    + `if (typeof module !== 'undefined') { module.exports = { vfs: vfs, fonts: fonts }; }\n`;

fs.writeFileSync(outputPath, source, 'utf8');
